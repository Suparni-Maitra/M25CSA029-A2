#Create Training Pairs
def create_skipgram_data(tokens, window_size=2):
    data = []
    for i in range(window_size, len(tokens) - window_size):
        target = tokens[i]
        # Context words are those within the window
        contexts = [tokens[i-j] for j in range(1, window_size+1)] + \
                   [tokens[i+j] for j in range(1, window_size+1)]
        for context in contexts:
            data.append((word_to_ix[target], word_to_ix[context]))
    return data

training_data = create_skipgram_data(all_tokens)
