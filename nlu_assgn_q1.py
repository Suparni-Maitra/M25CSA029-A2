class Word2VecFromScratch(nn.Module):
    def __init__(self, vocab_size, embedding_dim):
        super(Word2VecFromScratch, self).__init__()
        # Layer 1: Input to Hidden---The Embeddings
        self.embeddings = nn.Embedding(vocab_size, embedding_dim)
        # Layer 2: Hidden to Output-->Predicting context
        self.linear = nn.Linear(embedding_dim, vocab_size)

    def forward(self, inputs):
        # inputs: [batch_size]
        embeds = self.embeddings(inputs)
        out = self.linear(embeds)
        return out

# Initialize
EMBEDDING_DIM = 100
scratch_model = Word2VecFromScratch(vocab_size, EMBEDDING_DIM)

def train_scratch_model(model, data, epochs=20):
    optimizer = optim.Adam(model.parameters(), lr=0.01)
    loss_function = nn.CrossEntropyLoss()

    for epoch in range(epochs):
        total_loss = 0

        for target_idx, context_idx in data[:1000]:
            target_tensor = torch.tensor([target_idx])
            context_tensor = torch.tensor([context_idx])

            model.zero_grad()
            log_probs = model(target_tensor)
            loss = loss_function(log_probs, context_tensor)

            loss.backward()
            optimizer.step()
            total_loss += loss.item()

        print(f"Epoch {epoch+1}, Loss: {total_loss/10000:.4f}")

train_scratch_model(scratch_model, training_data)

def get_similarity(word1, word2, model):
    if word1 not in word_to_ix or word2 not in word_to_ix:
        return 0.0

    weights = model.embeddings.weight.data
    v1 = weights[word_to_ix[word1]]
    v2 = weights[word_to_ix[word2]]

   #COSINE SIMILARITY
    similarity = torch.dot(v1, v2) / (torch.norm(v1) * torch.norm(v2))
    return similarity.item()
