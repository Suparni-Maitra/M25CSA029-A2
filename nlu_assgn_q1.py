from sklearn.manifold import TSNE
import numpy as np
import matplotlib.pyplot as plt

def plot_embeddings_scratch(model, words, word_to_ix):

    embedding_dim = model.embeddings.weight.shape[1]
    arr = np.empty((0, embedding_dim), dtype='f')
    labels = []
    weights = model.embeddings.weight.data.cpu().numpy()

    for word in words:
        if word in word_to_ix:
            idx = word_to_ix[word]
            vector = weights[idx]
            arr = np.append(arr, [vector], axis=0)
            labels.append(word)
        else:
            print(f"Word '{word}' not found in vocabulary.")

    if len(labels) < 2:
        print("Error: Not enough words found to create a plot.")
        return

    perp = min(5, len(labels) - 1)
    tsne = TSNE(n_components=2, random_state=42, perplexity=perp, init='pca', learning_rate='auto')

    Y = tsne.fit_transform(arr)

    plt.figure(figsize=(10, 7))
    plt.scatter(Y[:, 0], Y[:, 1], color='red', edgecolors='black')

    for label, x, y in zip(labels, Y[:, 0], Y[:, 1]):
        plt.annotate(label, xy=(x, y), xytext=(5, 5), textcoords='offset points', fontsize=10)

    plt.title("t-SNE Visualization of IIT Jodhpur Word Clusters")
    plt.xlabel("Dimension 1")
    plt.ylabel("Dimension 2")
    plt.grid(True, linestyle='--', alpha=0.6)
    plt.show()

