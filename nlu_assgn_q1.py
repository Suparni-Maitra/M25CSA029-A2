import torch

def get_nearest_neighbors(word, model, word_to_ix, ix_to_word, top_n=10):
    if word not in word_to_ix:
        return f"Word '{word}' not in vocabulary."
    weights = model.embeddings.weight.data

    target_idx = word_to_ix[word]
    target_vec = weights[target_idx].unsqueeze(0)
    cos_sim = torch.nn.functional.cosine_similarity(target_vec, weights)

    scores, indices = torch.topk(cos_sim, top_n + 1)

    neighbors = []
    for i in range(1, len(indices)):
        idx = indices[i].item()
        neighbors.append((ix_to_word[idx], scores[i].item()))

    return neighbors

def run_scratch_analogies(model, a, b, c, word_to_ix, ix_to_word):

    for w in [a, b, c]:
        if w not in word_to_ix:
            print(f"Skipping analogy: '{w}' missing from vocab.")
            return

    weights = model.embeddings.weight.data
    vec_a = weights[word_to_ix[a]]
    vec_b = weights[word_to_ix[b]]
    vec_c = weights[word_to_ix[c]]

    # The Analogy Vector
    target_vec = (vec_b - vec_a + vec_c).unsqueeze(0)

    # Find closest word to this new vector
    cos_sim = torch.nn.functional.cosine_similarity(target_vec, weights)
    score, index = torch.topk(cos_sim, 1)

    print(f"Analogy {a}:{b} :: {c}:? => Result: {ix_to_word[index.item()]} (Score: {score.item():.4f})")

